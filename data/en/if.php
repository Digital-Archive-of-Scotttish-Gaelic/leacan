<h1>if</h1>
<p>The English word <strong>if</strong> has two main uses:</p>
<ol>
  <li>introducing a conditional-hypothetical subordinate clause</li>
  <li>introducing an embedded question.</li>
</ol>
<p>Here are some examples of <strong>if</strong> being used to introduce a conditional-hypothetical clause:</p>
<ul>
  <li>“I won’t take you to the police <mark>if</mark> you do a bit of work for me.”</li>
  <li>“<mark>If</mark> you stopped paying that, he would come to his senses.”</li>
  <li>“<mark>If</mark> you don't do that, it certainly won’t be me who comes to see you.”</li>
</ul>
<p>Note that the word <strong>if</strong> can be (somewhat clumsily) rephrased using ‘assuming that’ in these examples:</p>
<ul>
  <li>“I won’t take you to the police <mark>assuming that</mark> you do a bit of work for me.”</li>
  <li>“<mark>Assuming that</mark> you stopped paying that, he would come to his senses.”</li>
  <li>“<mark>Assuming that</mark> you don't do that, it certainly won’t be me who comes to see you.”</li>
</ul>
<p>Here are some examples of the second use of the word <strong>if</strong>, introducing an embedded question:</p>
<ul>
  <li>He asked <mark>if</mark> Maggie was in.</li>
  <li>He asked <mark>if</mark> Maggie wasn’t in.</li>
</ul>
<p>The word <strong>if</strong> can be easily replaced by ‘whether’ in these examples:</p>
<ul>
  <li>He asked <mark>whether</mark> Maggie was in.</li>
  <li>He asked <mark>whether</mark> Maggie wasn’t in.</li>
</ul>
<p>Or the whole embedded question could be turned into reported speech:</p>
<ul>
  <li>He asked, “Is Maggie in?”</li>
  <li>He asked, “Is Maggie not in?”</li>
</ul>
<p>When <strong>if</strong> is used in a conditional-hypothetical sense, it corresponds to one of the following Gaelic subordinating conjunctions:</p>
<ul>
  <li><strong><a href="../gd/conjunctions/ma.php">ma</a></strong> ‘if’</li>
  <li><strong><a href="../gd/conjunctions/mura.php">mura</a></strong> ‘if not’, ‘unless’</li>
  <li><strong><a href="../gd/conjunctions/nan.php">nan</a></strong> ‘if’</li>
</ul>
<p>For example:</p>
<ul>
  <li><strong>“Cha toir mi gu lagh thu <mark>ma</mark> nì thu car obrach dhòmhsa.”</strong><br/> 
  <small class="text-muted">“I won’t take you to the police if you do a bit of work for me.” [#2 29]</small></li>
  <li><strong>“<mark>Nan</mark> sguireadh tu a phàigheadh sin, thigeadh e gu chiall.”</strong><br/>
  <small class="text-muted">“If you stopped paying that, he would come to his senses.” [#2 7]</small></li>
  <li><strong>“<mark>Mura</mark> dèan thu sin, chan e mise a thig a choimhead ort idir.”</strong><br/>
  <small class="text-muted">“If you don't do that, it certainly won’t be me who comes to see you.” [#2: 55]</small></li>
</ul>
<p>When <strong>if</strong> is used to introduce an embedded question, it corresponds to embedded uses of the Gaelic interrogative verb particles:</p>
<ul>
  <li><strong><a href="../gd/particles/an.php">an</a></strong></li>
  <li><strong><a href="../gd/particles/nach.php">nach</a></strong></li>
</ul>
<p>For example:</p>
<ul>
  <li><strong>Dh’fhaighnich e <mark>an</mark> robh Magaidh a-staigh.</strong><br/>
  <small class="text-muted">‘He asked if Maggie was in.’ [#2 124]</small></li> 
  <li><strong>Dh’fhaighnich e <mark>nach</mark> robh Magaidh a-staigh.</strong><br/>
  <small class="text-muted">‘He asked if Maggie wasn’t in.’</small></li> 
</ul>
<p>Note that using <strong>ma</strong> or <strong>nan</strong> to introduce an embedded question is ☠️ <span class="badge badge-danger">Beurlachas</span> ☠️:</p>
<ul>
  <li>☠️ Dh’fhaighnich e <mark>ma</mark> bha Magaidh a-staigh. ☠️</li> 
  <li>☠️ Dh’fhaighnich e <mark>nan</mark> robh Magaidh a-staigh. ☠️</li> 
</ul>


